    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Usuario usuario = new Usuario();
        usuario.setApelido(apelido.getText());
        UsuarioDAOImpl crud = new UsuarioDAOImpl();
        if(id.getText().equals("")){
            if(crud.insere(usuario)){
                JOptionPane.showMessageDialog(null, "Usuário inserido com sucesso");
            }
            else{
                JOptionPane.showMessageDialog(null, "Usuário não inserido");
            }
        }
        else{
            usuario.setIdUsuario(Integer.parseInt(id.getText()));
            if(crud.atualiza(usuario)){
                JOptionPane.showMessageDialog(null, "Usuário atualizado com sucesso");
            }
            else{
                JOptionPane.showMessageDialog(null, "Usuário não atualizado");
            } 
        }       
    }//GEN-LAST:event_jButton1ActionPerformed

